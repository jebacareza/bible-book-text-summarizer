def remove_html(text):
    '''
    Removes HTML elements
    '''
    html = re.compile(r"<.*?>")
    return html.sub(r"", text)

def remove_emoji(string):
    '''
    Removes emojis
    '''
    emoji_pattern = re.compile(
        "["
        u"\U0001F600-\U0001F64F"  # emoticons
        u"\U0001F300-\U0001F5FF"  # symbols & pictographs
        u"\U0001F680-\U0001F6FF"  # transport & map symbols
        u"\U0001F1E0-\U0001F1FF"  # flags (iOS)
        u"\U00002702-\U000027B0"
        u"\U000024C2-\U0001F251"
        "]+",
        flags=re.UNICODE,
    )
    return emoji_pattern.sub(r"", string)

def remove_punct(text):
    '''
    Removes punctuations
    '''
    table = str.maketrans("", "", string.punctuation)
    #text = re.sub(r'[^a-zA-Z0-9 ]', '', text)
    return text.translate(table)

def remove_stopwords(text):
    '''
    Removes stop words and converts all characters to lower case
    '''
    text = [word.lower() for word in text.split() if word.lower() not in stop_words]

    return " ".join(text)

def lemmatizer(text):
    '''
    Takes in an a string as input, and outputs a lemmatized string
    '''    
    wnl = WordNetLemmatizer()
    token_list = nltk.word_tokenize(text)            
    lemmatized_list = ' '.join([wnl.lemmatize(words) for words in token_list])
    
    return lemmatized_list

